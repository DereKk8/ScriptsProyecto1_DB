CREATE VIEW ValorPoliza AS
SELECT
    P.ID AS ID_POLIZA, P.NUMERO, 
    (P.VALORASEGURABLE * (TC.PORCENTAJE / 100)) + (P.VALORASEGURABLE * (D.PORCENTAJE / 100))  AS VALOR_TOTAL_POLIZA
FROM POLIZA P
JOIN TIPO_CUBRIMIENTO TC ON P.IDTIPOCUBRIMIENTO = TC.ID
JOIN TOMADOR T ON P.IDTOMADOR = T.ID
JOIN DEPARTAMENTO D ON T.IDDEPARTAMENTO = D.ID;
ORDER BY P.ID ASC